            <h3>PIWONG MPC HISTORY</h3>
            <p>In the year 1985, Foster Parents Plan International identifies barangay Piwong to be one among the Barangays they would top to help in their program of activities. They recruited 51 heads of families in the community regarded as marginalized families. They started continuous training and dialog. Finally they came up with the idea of putting up a Savings and Credit Cooperative to address the common problems of poverty in the community. In December 2, 1986, this Coop was registered with Department of Agriculture, Cordillera Administrative Region, BPI Compound, Guise, and Baguio City with registered number R11-FF-201 to legalize its business operation. The capitalization was P 7,000.00 and a seed money of P 24,500.00 given by the Plan International Baguio.
	The following year, Piwong community became member of the Plan International Organization. At this point, trainings/seminars were conducted to the officers, staff and members on how to manage savings and credit cooperative.
	Now, Presidential Decree 175 and letter of Implementation 23 and each provision brings a ray of hope to the community people presenting a golden opportunity for the development and progress to this rich but undeveloped mountainous area. The fifty one members of the Plan International staff immediately drum up support for the amendments of the Piwong Savings and Credit Cooperative, to be known as PIWONG MULTI-PURPOSE COOPERATIVE. The core group has been formulated to conduct Survey, and feasibility studies followed by the election of officers. Then with the commitment of the officers, credit services started with a starting capital of  P 7,000.00 as an initial lending to the member of the cooperative.
	Today Piwong Multi-Purpose Cooperative is continuing to extend its help in developing the lives of its members not just in Ifugao province but also in other places through its branches at Maddela and Diffun Quirino, Bambang and Diadi, Nueva Vizcaya, Aurora Province and Baguio City.
